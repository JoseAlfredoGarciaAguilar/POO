public class mujer {
	
	public static void main(String[] args){
		String CESAR = "LESBIANA";
		String TUGFA = "COJAN";
		int VICTOR=4;
		
		if(CESAR=="LESBIANA"){
			for(int x=0; x<=8; x++){
				System.out.println("CESAR ES TRAVESTI");
			}
		}
		if(TUGFA=="COJAN"){
			for(int a=0; a<=VICTOR; a++){
				System.out.println("ABER TUS CNOS BBY");
			}
		}
		for(int m=0; m<8*VICTOR; m++){
			System.out.println("YA ME VINE");
		}
	}
}
